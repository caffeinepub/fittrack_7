export function searchLocalFood(query: string): FoodItem[] {
  const q = query.toLowerCase().trim();
  if (!q) return [];
  return FOOD_DATABASE.filter(
    (f) =>
      f.name.toLowerCase().includes(q) || f.category.toLowerCase().includes(q),
  ).slice(0, 12);
}

/** Return every unique category in the database, sorted. */
export function getAllCategories(): string[] {
  const seen = new Set<string>();
  for (const f of FOOD_DATABASE) seen.add(f.category);
  return Array.from(seen).sort();
}

/** Return all foods, optionally filtered to a specific category. */
export function getAllFoods(category?: string): FoodItem[] {
  if (!category || category === "All") return FOOD_DATABASE;
  return FOOD_DATABASE.filter((f) => f.category === category);
}
